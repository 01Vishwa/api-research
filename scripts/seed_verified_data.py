def _build_record(d: dict) -> AppRecord:
    return AppRecord(
        id=d["id"], app=d["app"], category=d["category"],
        one_line=d.get("one_line", ""),
        auth_methods=[AuthMethod(m) for m in d.get("auth_methods", [])],
        access_model=d.get("access_model", "unknown"),
        api_surface=APISurface(
            types=[APIType(t) for t in d.get("api_types", []) if t in [e.value for e in APIType]],
            breadth=Breadth(d.get("api_breadth", "unknown")) if d.get("api_breadth", "unknown") in [e.value for e in Breadth] else Breadth.UNKNOWN,
            webhooks=d.get("webhooks", False),
            rate_limits_documented=d.get("rate_limits_documented", False),
            notes=d.get("api_notes", ""),
        ),
        existing_mcp=MCPInfo(
            exists=d.get("existing_mcp", False),
            link=d.get("mcp_link"),
            official=d.get("mcp_official", False),
        ),
        developer_portal_url=d.get("developer_portal_url"),
        buildable_today=d.get("buildable_today"),
        blocker=d.get("blocker", "unknown"),
        evidence_url=d.get("evidence_url", ""),
        secondary_evidence_urls=d.get("secondary_urls", []),
        confidence=d.get("confidence", 0.5),
        verification_status=d.get("verification_status", "confirmed"),
        verifier_notes=d.get("verifier_notes", ""),
    )


